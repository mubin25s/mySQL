CREATE TABLE Schedules (
    ScheduleID INT PRIMARY KEY,
    CourseID INT,
    RoomID INT,
    DayOfWeek VARCHAR(10),
    TimeSlot VARCHAR(20),
    FOREIGN KEY (CourseID) REFERENCES Courses (CourseID),
    FOREIGN KEY (RoomID) REFERENCES Classrooms (RoomID)
);

